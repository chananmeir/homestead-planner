def validate_plant_database():
    """
    Validate plant database integrity at module load.
    Ensures all plants have required fields and consistent data.
    """
    required_fields = ['id', 'name', 'scientificName', 'category', 'spacing', 'daysToMaturity']
    valid_categories = ['vegetable', 'fruit', 'herb', 'flower', 'nut', 'cover-crop']

    errors = []
    plant_ids = set()

    for i, plant in enumerate(PLANT_DATABASE):
        plant_id = plant.get('id', f'index-{i}')

        # Check for required fields
        for field in required_fields:
            if field not in plant:
                errors.append(f"Plant '{plant_id}': Missing required field '{field}'")

        # Check for duplicate IDs
        if plant_id in plant_ids:
            errors.append(f"Plant '{plant_id}': Duplicate plant ID")
        plant_ids.add(plant_id)

        # Validate category
        if plant.get('category') not in valid_categories:
            errors.append(f"Plant '{plant_id}': Invalid category '{plant.get('category')}'")

        # Validate numeric fields
        if 'spacing' in plant and not isinstance(plant['spacing'], (int, float)):
            errors.append(f"Plant '{plant_id}': spacing must be numeric")

        if 'daysToMaturity' in plant and not isinstance(plant['daysToMaturity'], (int, float)):
            errors.append(f"Plant '{plant_id}': daysToMaturity must be numeric")

    if errors:
        print("[WARNING] Plant database validation warnings:")
        for error in errors[:10]:  # Show first 10 errors
            print(f"  - {error}")
        if len(errors) > 10:
            print(f"  ... and {len(errors) - 10} more errors")
    else:
        print(f"[OK] Plant database validated: {len(PLANT_DATABASE)} plants, all data valid")

    return len(errors) == 0

# Run validation when module is imported
validate_plant_database()

